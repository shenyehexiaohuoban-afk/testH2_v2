end
function value=mean_finite(x)
x=double(x(:));x=x(isfinite(x));if isempty(x),value=NaN;else,value=mean(x);end
end
function value=pct(x,p)
x=sort(double(x(:)));x=x(isfinite(x));if isempty(x),value=NaN;else,idx=max(1,min(numel(x),ceil(p/100*numel(x))));value=x(idx);end
end
