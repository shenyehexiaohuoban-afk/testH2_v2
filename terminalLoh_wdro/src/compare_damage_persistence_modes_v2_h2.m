function [lineViol, roadViol, slowViol] = monotonic_violation_counts(T, mode)
sub = T(T.damage_mode == mode, :);
groups = unique(sub(:, {'Rmax','a','loc','scenario_id'}), 'rows');
lineViol = 0;
roadViol = 0;
slowViol = 0;
for gg = 1:height(groups)
    q = sub(sub.Rmax == groups.Rmax(gg) & sub.a == groups.a(gg) & ...
        sub.loc == groups.loc(gg) & sub.scenario_id == groups.scenario_id(gg), :);
    q = sortrows(q, 'stage_index');
    lineViol = lineViol + any(diff(q.failed_line_count) < 0);
    roadViol = roadViol + any(diff(q.closed_road_count) < 0);
    slowViol = slowViol + any(diff(q.slowdown_severity_mean) < -1e-12);
end
end

function [pass, maxError] = check_D_total(T, H)
maxError = 0;
for ii = 1:height(H)
    q = T(T.damage_mode == H.damage_mode(ii) & T.Rmax == H.Rmax(ii) & ...
        T.Wstep == H.Wstep(ii) & T.a == H.a(ii) & T.loc == H.loc(ii) & ...
        T.scenario_id == H.scenario_id(ii), :);
    q = sortrows(q, 'stage_index');
    if height(q) ~= 3 || ~isequal(string(q.stage), ["W1";"W2";"W3"])
        pass = false;
        maxError = Inf;
        return;
    end
    maxError = max(maxError, abs(sum(q.D_slice_total_kg) - ...
        H.D_Hres3h_total_kg(ii)));
end
pass = maxError <= 1e-9;
end

